const express = require("express");
const invoiceController = require("../controllers/invoiceControllers");
const auth = require("../middleware/auth");
const router = express.Router();

/////////////////////////////////////////////
///////// CREATE INVOICE /////////////////////
///////////////////////////////////////////

router.route("/create").post(auth, invoiceController.createInvoice);

/////////////////////////////////////////////
///////// GET ALL INVOICE ///////////////////
///////////////////////////////////////////

router.route("/all").get(auth, invoiceController.getAllInvoice);

/////////////////////////////////////////////
///////// GET SINGLE INVOICE /////////////////
///////////////////////////////////////////

router.route("/get-single/:id").get(auth, invoiceController.getSingleInvoice);

///////////////////////////////////////////////
///////// GET INVOICE Of USER ///////////////////
/////////////////////////////////////////////

router.route("/user").get(auth, invoiceController.getAllUserInvoice);


module.exports = router;
